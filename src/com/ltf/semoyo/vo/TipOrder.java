package com.ltf.semoyo.vo;


public class TipOrder {
	private int no, tipNo;
	private String contents, pic;
	
	



	public TipOrder() {

	}





	public int getNo() {
		return no;
	}





	public void setNo(int no) {
		this.no = no;
	}
















	public int getTipNo() {
		return tipNo;
	}





	public void setTipNo(int tipNo) {
		this.tipNo = tipNo;
	}





	public String getContents() {
		return contents;
	}





	public void setContents(String contents) {
		this.contents = contents;
	}





	public String getPic() {
		return pic;
	}





	public void setPic(String pic) {
		this.pic = pic;
	}
	
	
	
	
	
}
