package com.ltf.semoyo.vo;


public class UseIngredient {
	private int no, reNo, inNo, gram, check;
	private String unit;
	
	//현추가
	private int checked;
	private String name;
	

	public UseIngredient() {

	}


	
	public int getChecked() {
		return checked;
	}



	public void setChecked(int checked) {
		this.checked = checked;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public int getNo() {
		return no;
	}


	public void setNo(int no) {
		this.no = no;
	}


	public int getReNo() {
		return reNo;
	}


	public void setReNo(int reNo) {
		this.reNo = reNo;
	}


	public int getInNo() {
		return inNo;
	}


	public void setInNo(int inNo) {
		this.inNo = inNo;
	}


	public int getGram() {
		return gram;
	}


	public void setGram(int gram) {
		this.gram = gram;
	}


	public int getCheck() {
		return check;
	}


	public void setCheck(int check) {
		this.check = check;
	}


	public String getUnit() {
		return unit;
	}


	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
	
	
}
