package com.ltf.semoyo.vo;

public class IceVO {
	
	private String nums, text;
	
	public IceVO() {
		
	}
	
	public IceVO(String nums, String text) {
		this.nums = nums;
		this.text = text;
	}

	public String getNums() {
		return nums;
	}

	public void setNums(String nums) {
		this.nums = nums;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}


	

}
