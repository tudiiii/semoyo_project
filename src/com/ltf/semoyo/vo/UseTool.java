package com.ltf.semoyo.vo;


public class UseTool {
	private int no, re_no, to_no, check;
	
	//현추가
	private int checked;
	private String name;
	



	public UseTool() {

	}





	public int getChecked() {
		return checked;
	}

	public void setChecked(int checked) {
		this.checked = checked;
	}


	public String getName() {
		return name;
	}





	public void setName(String name) {
		this.name = name;
	}





	public int getNo() {
		return no;
	}





	public void setNo(int no) {
		this.no = no;
	}





	public int getRe_no() {
		return re_no;
	}





	public void setRe_no(int re_no) {
		this.re_no = re_no;
	}





	public int getTo_no() {
		return to_no;
	}





	public void setTo_no(int to_no) {
		this.to_no = to_no;
	}





	public int getCheck() {
		return check;
	}





	public void setCheck(int check) {
		this.check = check;
	}
	
	
	
	
	
}
