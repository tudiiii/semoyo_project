package com.ltf.semoyo.vo;


public class Tool {
	private int no;
	private String name, image;

	



	public Tool() {

	}





	public int getNo() {
		return no;
	}





	public void setNo(int no) {
		this.no = no;
	}





	public String getName() {
		return name;
	}





	public void setName(String name) {
		this.name = name;
	}





	public String getImage() {
		return image;
	}





	public void setImage(String image) {
		this.image = image;
	}





	
	
	
	
	
}
